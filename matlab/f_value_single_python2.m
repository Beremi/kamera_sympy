function [res1, res2] = f_value_single_python2(alpha, beta, gama, X, Y, Z, L, v1, v2, v3, n1, n2, n3, ff, S_x, S_y, S_z)
    x0 = sin(beta);
    x1 = v3*x0;
    x2 = cos(beta);
    x3 = cos(gama);
    x4 = v1*x2*x3;
    x5 = sin(gama);
    x6 = -v2*x2*x5 + x1 + x4;
    x7 = cos(alpha);
    x8 = v3*x2;
    x9 = x7*x8;
    x10 = sin(alpha);
    x11 = x10*x3;
    x12 = x5*x7;
    x13 = v2*(x0*x12 + x11);
    x14 = x10*x5;
    x15 = x3*x7;
    x16 = v1*(-x0*x15 + x14);
    x17 = x13 + x16 + x9;
    x18 = n1*x17 - n3*x6;
    x19 = x10*x8;
    x20 = v1*(x0*x11 + x12);
    x21 = x0*x14 - x15;
    x22 = -v2*x21;
    x23 = -x19 + x20 + x22;
    x24 = -n1*x23 + n2*x6;
    x25 = n2*x17;
    x26 = -X;
    x27 = sqrt(v1^2 + v2^2 + v3^2)*abs(Z/x17);
    x28 = x27*x6;
    x29 = -L*v2*x2*x5 + L*x1 + L*x4 + x26;
    x30 = -Y;
    x31 = -L*v3*x10*x2 + L*x20 + L*x22 + x30;
    x32 = -Z;
    x33 = L*x13 + L*x16 + L*x9 + x32;
    x34 = S_x*x6 + S_y*x23 + S_z*x17;
    x35 = x23*x27;
    x36 = x17*x27;
    x37 = (x17*(Z + x36) + x23*(Y + x35) - x34 + x6*(X + x28))/(-x17*x33 - x23*x31 - x29*x6 - x34);
    x38 = S_x + x26 - x28 + x37*(-S_x - x29);
    x39 = S_y + x30 - x35 + x37*(-S_y - x31);
    x40 = S_z + x32 - x36 + x37*(-S_z - x33);
    x41 = ff/(L - ff + x27);
    res1 = x41*(x18*x39 + x24*x40 + x38*(n3*x23 - x25))/(x18^2 + x24^2 + (n3*(v2*x21 + x19 - x20) + x25)^2);
    res2 = x41*(n1*x38 + n2*x39 + n3*x40)/(n1^2 + n2^2 + n3^2);
end
